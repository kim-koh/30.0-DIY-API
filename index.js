import express from "express";
import bodyParser from "body-parser";

const app = express();
const port = 3000;
const masterKey = "4VGP2DN-6EWM4SJ-N6FGRHV-Z3PR3TT";

app.use(bodyParser.urlencoded({ extended: true }));

//1. GET a random joke
app.get("/random", (req, res) => {
  const index = Math.floor(Math.random() * jokes.length);
  res.json(jokes[index]);
});

//2. GET a specific joke
app.get("/jokes/:id", (req, res) => {
  try {
    const requestedID = parseInt(req.params.id);
    if (requestedID > jokes.length) {
      console.log(requestedID)
      res.send("The joke with the ID you requested does not exist."); 
    } else {
      const requestedJoke = jokes.find((joke) => joke.id === requestedID);
      res.json(requestedJoke);
    }
    
  } catch (error) {
    console.log(error)
    res.send("Oops! Something went wrong with your request.")
  }
}); 

//3. GET a jokes by filtering on the joke type
app.get("/filter", (req, res) => {
  const chosenType = req.query.type
  try {
    const requestedJokes = jokes.filter((joke) => joke.jokeType === chosenType)
    res.send(requestedJokes)
  } catch (error) {
    res.send(error)
  }
}); 

//4. POST a new joke
app.post("/jokes", (req, res) => {
  const newJoke = {
    id: jokes.length + 1,
    jokeText: req.query.text,
    jokeType: req.query.type}; 
  jokes.push(newJoke); 
  res.json(newJoke);
});

//5. PUT a joke
app.put("/jokes/:id", (req, res) => {
  const requestedID = parseInt(req.params.id);
  const requestedJoke = jokes.find((joke) => joke.id === requestedID);
  const newText = req.query.text;
  const newType = req.query.type; 
  if (requestedJoke === undefined) {
    res.send("There is no joke with this id in the database");
  } else if (newText === undefined || newType === undefined) {
    res.send("You must provide both 'text' and 'type' for the joke you are editing"); 
  } else {
    jokes[requestedID -1] = {
      id: requestedID,
      jokeText: newText,
      jokeType: newType
    };
  res.send(jokes.find((joke) => joke.id === requestedID));
  }
})

//6. PATCH a joke
app.patch("/jokes/:id", (req, res) => {
  const requestedID = parseInt(req.params.id);
  const requestedJoke = jokes.find((joke) => joke.id === requestedID);
  const newText = req.query.text;
  const newType = req.query.type; 
  if (requestedJoke === undefined) {
    res.send("There is no joke with this id in the database")
  } else if (newText === undefined && newType === undefined) {
    res.send("Please provide either the new text or type that you would like to edit for this joke")
  } else if (newText === undefined) {
    jokes[requestedID -1] = {
      id: requestedID,
      jokeText: requestedJoke.jokeText,
      jokeType: newType
    };
  res.send(jokes.find((joke) => joke.id === requestedID));
  } else if (newType === undefined) {
    jokes[requestedID -1] = {
      id: requestedID,
      jokeText: newText,
      jokeType: requestedJoke.jokeType
    };
    res.send(jokes.find((joke) => joke.id === requestedID));
  }
});

//7. DELETE Specific joke
app.delete("/jokes/:id", (req, res) => {
  const id = req.params.id;
  try {
    const index = jokes.findIndex((joke) => joke.id === id);
    jokes.splice(index, 1); 
    res.send(jokes)
  } catch (error) {
    console.log(error); 
    console.log(`the id the user requested was: ${id}`); 
    res.send("Failed")
  } 
});

//8. DELETE All jokes
app.delete("/all", (req, res) => {
  const authKey = req.query.key; 
  if (authKey === "masterKey") {
    jokes = [];
    res.send(jokes);
  } else {
    res.send("You do not have authorization to complete this action.")
  }
});

